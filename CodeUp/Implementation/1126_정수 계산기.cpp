#include <iostream>
using namespace std;
int main()
{
	int inputA, inputB;
	cin >> inputA >> inputB;
	cout << inputA << " + " << inputB << " = " << inputA + inputB << endl;
	cout << inputA << " - " << inputB << " = " << inputA - inputB << endl;
	cout << inputA << " * " << inputB << " = " << inputA * inputB << endl;
	cout << inputA << " / " << inputB << " = " << inputA / inputB << endl;
	cout << inputA << " % " << inputB << " = " << inputA % inputB << endl;
	return 0;
}